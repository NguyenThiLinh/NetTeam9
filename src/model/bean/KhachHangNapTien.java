package model.bean;

public class KhachHangNapTien {
	private String userName;
	private float soTienNap;
	private String ngayNap;
	public KhachHangNapTien() {
		super();
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public float getSoTienNap() {
		return soTienNap;
	}
	public void setSoTienNap(float soTienNap) {
		this.soTienNap = soTienNap;
	}
	public String getNgayNap() {
		return ngayNap;
	}
	public void setNgayNap(String ngayNap) {
		this.ngayNap = ngayNap;
	}
	
	
}


